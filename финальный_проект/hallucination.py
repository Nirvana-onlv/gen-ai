"""
hallucination.py
----------------
Проверяет сгенерированный ответ продавца на галлюцинации:
выдуманные факты, обещания и цифры которых не было в оригинальном отзыве.
"""
from __future__ import annotations

from llm_client import make_client, get_model
from schema import HallucinationReport, ReviewInput, SellerReply

_CLIENT = make_client()


class HallucinationChecker:
    """
    LLM-судья который сверяет ответ продавца с оригинальным отзывом.

    Ищет три типа галлюцинаций:
      1. Ghost-цитаты  — ссылки на слова покупателя которых не было в тексте
      2. Выдуманные факты — конкретные детали (сроки, артикулы, цифры)
                            которые модель добавила от себя
      3. Ложные обещания — обязательства которые не вытекают из контекста отзыва
    """

    SYSTEM = """Ты эксперт по верификации текстов.

Тебе дан оригинальный отзыв покупателя и ответ продавца.
Найди в ответе продавца галлюцинации — факты, детали или обещания
которых НЕТ в оригинальном отзыве и которые модель добавила от себя.

Три типа галлюцинаций:
1. Ghost-цитаты — ответ ссылается на слова покупателя которых не было
   Пример: «Вы упомянули что товар пришёл мокрым» — если этого не было в отзыве
2. Выдуманные факты — конкретные цифры, сроки, артикулы которых не было
   Пример: «Оформим замену в течение 24 часов» — если срок нигде не фигурировал
3. Ложные обещания — обязательства не вытекающие из контекста
   Пример: «Вернём деньги в полном объёме» — если возврат не обсуждался

НЕ считай галлюцинацией:
- Стандартные вежливые фразы («Приносим извинения», «Ценим ваше мнение»)
- Общие предложения связаться с поддержкой
- Информацию о товаре которая логично вытекает из контекста

Будь строгим: выдуманная конкретная цифра или точная дата ответа — всегда галлюцинация."""

    def run(
        self,
        review: ReviewInput,
        reply: SellerReply,
        max_retries: int = 2,
    ) -> HallucinationReport:
        user_content = (
            f"Оригинальный отзыв покупателя:\n{review.text}\n\n"
            f"Ответ продавца для проверки:\n{reply.reply_text}"
        )

        messages = [
            {"role": "system", "content": self.SYSTEM},
            {"role": "user", "content": user_content},
        ]

        return _CLIENT.chat.completions.create(
            model=get_model(),
            messages=messages,
            response_model=HallucinationReport,
            max_retries=max_retries,
        )